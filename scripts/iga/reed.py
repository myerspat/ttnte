    return (
        1.0
        - 5.96168047527760e-47 * np.cosh(52.06761235859028 * x)
        - 6.78355315350872e-56 * np.cosh(62.76152118553390 * x)
        - 7.20274049646598e-84 * np.cosh(95.14161078659372 * x)
        - 6.34541150517664e-238 * np.cosh(272.5766481169758 * x)
    )


