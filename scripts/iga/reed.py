import numpy as np


def reed(x):
    flux = np.zeros(x.shape)

    # Fuel
    flux[(0 <= x) & (x < 2)] = _region_1(x[(0 <= x) & (x < 2)])

    # Can
    flux[(2 <= x) & (x < 3)] = _region_2(x[(2 <= x) & (x < 3)])

    # Void
    flux[(3 <= x) & (x < 5)] = _region_3(x[(3 <= x) & (x < 5)])

    # Moderator
    flux[(5 <= x) & (x < 6)] = _region_4(x[(5 <= x) & (x < 6)])

    # Moderator
    flux[(6 <= x) & (x <= 8)] = _region_5(x[(6 <= x) & (x <= 8)])

    return flux


